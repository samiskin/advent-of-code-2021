fn main() {
    let input = _get_input();
    let numbers = input
        .trim()
        .split("\n")
        .map(|s| s.split(" "))
        .map(|mut split| {
            (
                split.next().unwrap(),
                split.next().unwrap().parse::<i32>().unwrap(),
            )
        })
        .collect::<Vec<(&str, i32)>>();

    let p1 = numbers
        .iter()
        .map(|(dir, amt)| match *dir {
            "forward" => (*amt, 0),
            "up" => (0, *amt),
            "down" => (0, -*amt),
            _ => panic!(),
        })
        .fold((0, 0), |(ix, iy), (dx, dy)| (ix + dx, iy + dy));

    let p2 = numbers
        .iter()
        .map(|(dir, amt)| match *dir {
            "forward" => (*amt, 0),
            "up" => (0, *amt),
            "down" => (0, -*amt),
            _ => panic!(),
        })
        .fold((0, 0, 0), |(ix, iy, ia), (dx, da)| {
            (ix + dx, iy + dx * ia, ia + da)
        });

    println!("Part 1: {:?}", p1.0 * p1.1);
    println!("Part 2: {:?}", p2.0 * p2.1);
}

