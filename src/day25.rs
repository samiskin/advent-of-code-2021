mod utils;
use utils::Grid;
use std::collections::HashSet;
fn main() {
    // let input = _get_test_input();
    let input = _get_input();

    // ----------- Parse Input -----------

    let parsed = input
        .trim()
        .split('\n')
        .map(|l| l.trim().chars().collect())
        .collect::<Vec<Vec<char>>>();

    let grid = Grid::new(parsed);

    // ----------- Solve -----------
    fn step(grid: &Grid<char>) -> Grid<char> {
        let mut next_grid = grid.clone();
        next_grid.set_all('.');

        for ((x, y), c) in grid.iter() {
            match c {
                '>' => {
                    if x < grid.width() - 1 && *grid.get(x + 1, y) == '.' {
                        next_grid.set(x + 1, y, '>');
                    } else if x == grid.width() - 1 && *grid.get(0, y) == '.' {
                        next_grid.set(0, y, '>');
                    } else {
                        next_grid.set(x, y, '>');
                    }
                },
                _ => {}
            };
        }

        for ((x, y), c) in grid.iter() {
            match c {
                'v' => {
                    if y < next_grid.height() - 1 && *next_grid.get(x, y + 1) != '>' && *grid.get(x, y + 1) != 'v' {
                        next_grid.set(x, y + 1, 'v');
                    } else if y == next_grid.height() - 1 && *next_grid.get(x, 0) != '>' && *grid.get(x, 0) != 'v' {
                        next_grid.set(x, 0, 'v');
                    } else {
                        next_grid.set(x, y, 'v');
                    }
                },
                _ => {}
            };
        }

        next_grid
    } 

    let mut curr = grid;
    let mut steps = 0;
    loop {
        steps += 1;
        let next = step(&curr);
        if next == curr {
            break;
        }
        curr = next;
    }


    // ----------- Print -----------

    println!("Part 1: {:?}", steps);
}

